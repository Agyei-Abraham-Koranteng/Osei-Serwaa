-- Function to track visit (Log + Increment Daily)
create or replace function track_visit(
  p_user_agent text,
  p_browser text,
  p_device_type text,
  p_os text
)
returns void
language plpgsql
security definer
as $$
begin
  -- 1. Log the individual visit
  insert into public.visitor_logs (user_agent, browser, device_type, os)
  values (p_user_agent, p_browser, p_device_type, p_os);

  -- 2. Increment daily counter
  insert into public.daily_visitors (date, count)
  values (CURRENT_DATE, 1)
  on conflict (date) do update set count = daily_visitors.count + 1, updated_at = now();
  
  -- 3. Increment total counter (legacy support, optional but good for consistency)
  insert into public.site_content (key, value)
  values ('site_visitors', json_build_object('count', 1))
  on conflict (key) do update set value = json_build_object('count', ((site_content.value->>'count')::int + 1));
end;
$$;

