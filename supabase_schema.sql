-- Note: Public needs to update 'site_visitors' key specifically? 
-- Ideally, use a stored procedure (RPC) for incrementing visitors to avoid opening up write access.
-- For now, allowing public update on 'site_visitors' key only could be risky without strict checks.
-- Better approach: Create a Postgres Function for incrementing visitors.

-- Function to increment visitors safely
create or replace function increment_visitors()
returns json
language plpgsql
security definer
as $$
declare
  current_count integer;
  new_count integer;
begin
  select (value->>'count')::integer into current_count from public.site_content where key = 'site_visitors';
  if current_count is null then
    current_count := 0;
  end if;
  new_count := current_count + 1;
  
  insert into public.site_content (key, value)
  values ('site_visitors', json_build_object('count', new_count))
  on conflict (key) do update set value = json_build_object('count', new_count);
  
  return json_build_object('count', new_count);
end;
$$;

-- Function to reset visitors (Admin only)
create or replace function reset_visitors()
returns json
language plpgsql
security definer
as $$
begin
  if auth.role() != 'authenticated' then
    raise exception 'Not authorized';
  end if;

  insert into public.site_content (key, value)
  values ('site_visitors', json_build_object('count', 0))
  on conflict (key) do update set value = json_build_object('count', 0);
  
  return json_build_object('count', 0, 'message', 'Visitor count reset');
end;
$$;

